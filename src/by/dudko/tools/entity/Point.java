package by.dudko.tools.entity;

import by.dudko.tools.constants.Constants;

import java.util.Random;

/**
 * Created by cplus on 14.05.2017.
 */
public class Point {

    private double x;
    private double y;
    private Random random = new Random();

    public Point() {
    }

    public Point(double x, double y) {
        this.x = x;
        this.y = y;
    }

    public double getX() {
        return x;
    }

    public void setX(double x) {
        this.x = x;
    }

    public double getY() {
        return y;
    }

    public void setY(double y) {
        this.y = y;
    }

    public void setRandomX(){
        x = random.nextInt(Constants.PANEL_WIDTH - 50);
    }

    public void setRandomY(){
        y = random.nextInt(Constants.PANEL_HEIGHT - 50);
    }

    public void setRandomCoordinates(){
        setRandomX();
        setRandomY();
    }

    @Override
    public boolean equals(Object o) {
        if (this == o) return true;
        if (o == null || getClass() != o.getClass()) return false;

        Point point = (Point) o;

        if (Double.compare(point.x, x) != 0) return false;
        return Double.compare(point.y, y) == 0;
    }

    @Override
    public int hashCode() {
        int result;
        long temp;
        temp = Double.doubleToLongBits(x);
        result = (int) (temp ^ (temp >>> 32));
        temp = Double.doubleToLongBits(y);
        result = 31 * result + (int) (temp ^ (temp >>> 32));
        return result;
    }

    @Override
    public String toString() {
        return "Point{" +
                "x=" + x +
                ", y=" + y +
                '}';
    }
}
